


// Using var (old way, function-scoped)
var name = "Nhlanhla";

// Using let (modern, block-scoped, reassignable)
let age = 51;

// Using const (block-scoped, cannot be reassigned)
const country = "South Africa";

console.log(name);    // Nhlanhla
console.log(age);     // 51
console.log(country); // South Africa
